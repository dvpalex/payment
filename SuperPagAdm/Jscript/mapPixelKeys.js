var fontsValidas = "verdana;geneva;";

function changeFont( control, maskControl )
{
	if ( fontsValidas.indexOf(maskControl.fontname) == -1 ) {
		control.style.fontFamily = "verdana";
		control.style.fontSize = "xx-small";
		maskControl.fontname = control.style.fontFamily;
		maskControl.fontsize = control.style.fontSize;
	}
}

function verificaPosicao( texto, valor, scroll, fontname, fontsize )
{

	var soma = valor + scroll;
	//sempre começa do pixel 1
	var checkpos = 1;
	
	var _nfontsize = parseInt( fontsize.replace(/[^0-9]/gi, ""), 10);
	if ( isNaN(_nfontsize) )
		_nfontsize = arrayFontesTamIE[fontsize.toString()];
		
	if ( soma == checkpos ) {
		return 0;
	}
	
	//pega array da fonte usada pelo controle
	var arrayFonte;
	var fontnameValidada = fontname.replace(/[^a-zA-Z0-9]/gi, "_");
	
	eval( " arrayFonte = array_" + fontnameValidada + ";" );
	
	for(i=0; i<texto.length; i++) {
		var charct = texto.charAt(i);

		if ( arrayFonte[charct][_nfontsize] != undefined ) {
			checkpos += ( parseInt( arrayFonte[charct][_nfontsize], 10 ) );
		} else {
			var x1 = charct.charCodeAt(0);
			var ss = x1.toString(16);
			if ( arrayFonte[ss][_nfontsize] != undefined )
				checkpos += ( parseInt( arrayFonte[ss][_nfontsize] , 10 ) );
		}
		
		if ( checkpos >= soma ) {			
			return i+1;
		}
	}
}

